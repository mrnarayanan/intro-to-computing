; This program is similar to Lab 13, but it prints a string of characters instead of just a single
;	one. The program prints line by line rather than character by character. First, the program
;	prints the first line of the first character, then iterates to the first line of the second
;	character, and so on until the end of the string. Then, the line counter increments and the
;	program iterates through all of the characters again, this time printing the second row. 
;	This continues until all 16 lines of every character in the string is printed. 

; Register Table
; R0 - used to hold character for OUT function, and also temporary computations
; R1 - holds address for letters in string to print
; R2 - holds specific letter to print, temporarily hold address of FONT_DATA, and later a counter
; R3 - counter used for multiplication
; R4 - holds address of line to print in FONT_DATA
; R5 - holds data of FONT_DATA specified in address in R4
; R6 - holds address of correct bitmask, and temporarily the line count offset
; R7 - holds values for temporary computations

	.ORIG	x3000
TOP	AND R0,R0,#0 ; clear all registers
	AND R1,R1,#0
	AND R2,R2,#0
	AND R3,R3,#0
	AND R4,R4,#0
	AND R5,R5,#0
	AND R6,R6,#0
	LD R1,FTWO ; loads address x5002, start of string
LOOP	LDR R2,R1,#0 ; loads value at address in R1
	BRz END ; if character is NULL (x0000) branch to end of loop
	AND R4,R4,#0 ; clears R4	
	ADD R3,R3,#8 ; initializes multiplication counter to #16
	ADD R3,R3,#8	
MULT	BRz EOL ; this loop multiplies the value in R2 by #16
	ADD R4,R4,R2 ; adds R2 to R4 and stores result in R4
	ADD R3,R3,#-1 ; decrement multiplication counter
	BRnzp MULT
EOL	LEA R2,FONT_DATA ; loads address of FONT_DATA into R2
	LD R6,COUNT ; loads and adds line count offset #(0-15) to FONT_DATA pointer
	ADD R4,R4,R6 ;
	ADD R4,R4,R2 ; R4 now holds address of line to be printed of FONT_DATA
	LDR R5,R4,#0 ; R5 now holds data of line to be printed of FONT_DATA	
	AND R2,R2,#0 ; resets R2 to 0
	ADD R2,R2,#8 ; initializes inner loop counter to #8
	LEA R6,BITMASK ; loads address of first bitmask into R6
INNER	LDR R0,R6,#0 ; loads bitmask into R0
	AND R0,R5,R0 ; ANDs bitmask with data in FONT_DATA row
	ADD R0,R0,#-1 ; adds negative 1 (xFFFF) to bitmasked value, this tests if bit is 0 or 1
	BRzp PRTONE
	LDI R0,FZERO ; loads and prints character representing 0
	OUT
	BRnzp AFTER
PRTONE	LDI R0,FONE ; loads and prints character representing 1
	OUT
AFTER	ADD R6,R6,#1 ; increments to point to next bitmask
	ADD R2,R2,#-1 ; decrement inner loop counter by #-1
	BRnp INNER ; if counter not zero, repeat inner loop
	ADD R1,R1,#1 ; increments pointer to letters in string to print
	BRnzp LOOP ; 
END	LD R0,LINE ; loads and prints newline character
	OUT	
	LD R0,COUNT ; increments line offset counter and stores back into memory
	ADD R0,R0,#1 
	ST R0,COUNT 
	LD R1,SIXT ; loads #-16
	ADD R0,R0,R1 ; checks if ran 16 times (to see if 16 lines were printed)
	BRnp TOP ; if haven't run 16 times, go back to start of outer loop
	HALT


BITMASK	.FILL	x8000
	.FILL	x4000
	.FILL	x2000
	.FILL	x1000
	.FILL	x0800
	.FILL	x0400
	.FILL	x0200
	.FILL	x0100

FZERO	.FILL	x5000
FONE	.FILL	x5001
FTWO	.FILL	X5002
	
LINE	.FILL	x000A
SIXT	.FILL	#-16
COUNT	.FILL	x0000


