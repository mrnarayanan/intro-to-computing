; This program prints a "picture" of an ASCII character using other characters. The characters
;	used to print the "ink", "spaces", and the character of the picture are specified using
;	a script. The program uses bitmasks and simple addition to determine whether a given
;	bit is a 0 or 1, and then prints the corresponding character. There is an inner loop that
;	iterates over the 8 bits in each line, and an outer loop that iterates over the 16 lines
;	that need to be printed. The character to be printed is found within FONT_DATA by multiplying
;	its ASCII code by 16, and adding it the starting address FONT_DATA. 

; Register Table
; R0 - contains value in location x5000 (ASCII char), and later holds bitmasks, and chars to print
; R1 - contains value in location x5001 (ASCII char), and later holds final value that tests if 0 or 1
; R2 - contains value in location x5002 (ASCII char), and later becomes a counter
; R3 - counter
; R4 - contains address of particular row of FONT_DATA
; R5 - contains data in particular row of FONT_DATA
; R6 - contains memory address x5000, and later holds address to correct bitmask

	.ORIG	x3000
	AND R0,R0,#0 ; clear all registers
	AND R1,R1,#0
	AND R2,R2,#0
	AND R3,R3,#0
	AND R4,R4,#0
	AND R5,R5,#0
	AND R6,R6,#0
	LD R6,FIVEK ; loads the value x5000 into R6
	LDR R0,R6,#0 ; loads value at x5000 into R0
	LDR R1,R6,#1 ; loads value at x5001 into R1
	LDR R2,R6,#2 ; loads value at x5002 into R2
	ADD R3,R3,#8 ; initializes counter to #16
	ADD R3,R3,#8
MULT	BRz EOL ; this loop multiplies the value in R2 by #16
	ADD R4,R4,R2 ; adds R2 to R4 and stores result in R4
	ADD R3,R3,#-1;
	BRnzp MULT
EOL	LEA R2,FONT_DATA ; loads address of FONT_DATA into R2	
	ADD R4,R4,R2 ; R4 now holds address of first line to be printed of FONT_DATA
	LDR R5,R4,#0 ; R5 now holds data of first line to be printed of FONT_DATA
	AND R2,R2,#0 ; resets R2 to 0
	ST R0,ZERO ; stores character to print for 0 in memory to be accessed later
	ST R1,ONE ; stores character to print for 0 in memory to be accessed later
	ADD R3,R3,#8 ; initializes outer loop counter to #16
	ADD R3,R3,#8
OUTER	ADD R2,R2,#8 ; initializes inner loop counter to #8
	LEA R6,BITMASK ; loads address of first bitmask into R6
INNER	LDR R0,R6,#0 ; loads bitmask into R0
	AND R1,R5,R0 ; ANDs bitmask with data in FONT_DATA row
	ADD R1,R1,#-1 ; adds negative 1 (xFFFF) to bitmasked value, this tests if bit is 0 or 1
	BRzp PRTONE
	LD R0,ZERO ; loads and prints character representing 0
	OUT
	BRnzp AFTER
PRTONE	LD R0,ONE ; loads and prints character representing 1
	OUT
AFTER	ADD R6,R6,#1 ; increments to point to next bitmask
	LDR R0,R6,#0 ; loads next bitmask into R0
	ADD R2,R2,#-1 ; decrement inner loop counter by #-1
	BRnp INNER ; if counter not zero, repeat inner loop
	LD R0,LINE ; loads and prints newline character
	OUT
	ADD R4,R4,#1 ; increments to point to next line in FONT_DATA
	LDR R5,R4,#0 ; loads data from next line in FONT_DATA into R5
	ADD R3,R3,#-1 ; decrement outer loop counter by #-1
	BRnp OUTER ; if counter not zero, repeat outer loop
	HALT

BITMASK	.FILL	x8000
	.FILL	x4000
	.FILL	x2000
	.FILL	x1000
	.FILL	x0800
	.FILL	x0400
	.FILL	x0200
	.FILL	x0100

FIVEK	.FILL	x5000
LINE	.FILL	x000A
ZERO	.FILL	x0000
ONE	.FILL	x0000

